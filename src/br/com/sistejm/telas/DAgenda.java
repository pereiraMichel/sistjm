    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        ag = new Agenda();
        if(!txtId.getText().equals("")){
            if(JOptionPane.showConfirmDialog(null, "Confirma exclus√£o? ", "Exclus√£o", JOptionPane.YES_NO_OPTION) == 0){
                ag.setIdagenda(Integer.valueOf(txtId.getText()));
                ag.excluirAgenda();
            }
        }else{
            preencheTexto("\nSelecione a agenda para a exclus√£o");
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

