    public void recebeInfo(int idMedium, int codUsuario, String medium, String dataEntrada){

        coroa = new Coroa();
        bat = new Batismo();
        
        String nomeMaiusc = medium;
        txtCodMedium.setText(String.valueOf(idMedium));
        txtCodUser.setText(String.valueOf(codUsuario));
        labelNomeMedium.setText(nomeMaiusc.toUpperCase());
        labelDataEntrada.setText(dataEntrada);
        
        //alimenta coroa
        coroa.setCodmedium(idMedium);
        coroa.verificaCamposPadrao();
        
        if(!coroa.confereBatismoCoroa()){
            coroa.setAno(0);
            coroa.setMes(0);
            coroa.setConfirma("n");
            coroa.setDataRealizacao("01/01/1901");
            coroa.setCodmedium(idMedium);
            coroa.setCodtipocoroa(1);
            coroa.insereCoroa();
        }
        
//        //alimenta batismo
        bat.setCodMedium(idMedium);
        bat.verificaExistente();
        
        tabelaMediuns();
        
        consultaMedium();

    }    
    
