    private void txtBatismoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBatismoKeyReleased
        coroa = new Coroa();
        bat = new Batismo();
        
        if(txtBatismo.getText().length() == 2){
            txtBatismo.setText(txtBatismo.getText() + "/");
        }
        if(txtBatismo.getText().length() == 5){//estou no mes
            txtBatismo.setText(txtBatismo.getText() + "/");
        }
        if(txtBatismo.getText().length() == 10){//estou no ano
            this.mes = Integer.valueOf(txtBatismo.getText().substring(3, 5));
            this.ano = Integer.valueOf(txtBatismo.getText().substring(6, 10));
            
            coroa.setAno(this.ano);
            coroa.setMes(this.mes);
            coroa.setConfirma("s");
            coroa.setDataRealizacao(txtBatismo.getText());
            coroa.setCodtipocoroa(1);
            coroa.setCodmedium(Integer.valueOf(txtCodMedium.getText()));
            coroa.marcaCoroa();
            
            
            // altera batismo
            bat.setCodMedium(Integer.valueOf(txtCodMedium.getText()));
            bat.setDataBatismo(txtBatismo.getText());
            bat.setPadrinho(txtPadrinho.getText());
            bat.setMadrinha(txtMadrinha.getText());
            bat.alterarBatismoMedium();
            
            consultaMedium();
  
        }
        
    }//GEN-LAST:event_txtBatismoKeyReleased

