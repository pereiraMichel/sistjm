    private void txtAno2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAno2KeyReleased

        if(txtAno2.getText().length() == 4){
            if(JOptionPane.showConfirmDialog(this, "Confirma a impress√£o ?", "Imprimir", JOptionPane.YES_NO_OPTION) == 0){
                r = new Relatorios();
                r.geraAnosCarteira(Integer.valueOf(txtAno1.getText()), Integer.valueOf(txtAno2.getText()));
            }
        }

    }//GEN-LAST:event_txtAno2KeyReleased

