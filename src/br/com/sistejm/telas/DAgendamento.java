    private void txtDataKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDataKeyPressed

        config = new Configuracoes();

        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            if(!txtData.getText().equals("")){
                String content = txtData.getText();
                int ano = Integer.valueOf(content.substring(6, 10));
                int mes = Integer.valueOf(content.substring(3, 5));
                int dia = Integer.valueOf(content.substring(0, 2));

                config.retornaSemana(dia, mes, ano, labelSemana);
                txtValor.requestFocus();
            }

        }
        // TODO add your handling code here:
    }//GEN-LAST:event_txtDataKeyPressed

