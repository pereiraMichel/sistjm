    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        ag = new Agendamento();
        con = new Conexao();
        
        if(radioCaboclo.isSelected()){
            this.setConsultor("Caboclo");
        }
        if(radioExu.isSelected()){
            this.setConsultor("Exu");
        }
        
        if(radioSim.isSelected()){
            this.setPago("S");
        }
        if(radioNao.isSelected()){
            this.setPago("N");
        }
        
        if(radioNMedium.isSelected()){
            this.setTconsultado("N");
        }
        if(radioMedium.isSelected()){
            this.setTconsultado("S");
        }
        
        String valorPonto = txtValor.getText().replace(",", ".");

        ag.setData(txtData.getText());
        ag.setConsultado(txtNome.getText());
        ag.setConsultor(this.getConsultor());
        ag.setTConsultado(this.getTconsultado());
        ag.setCodusuario(codusuario);
        ag.setValor(Double.parseDouble(valorPonto));
        ag.setPago(this.pago);
        ag.setSemana(labelSemana.getText());
        ag.setCodigo(txtConsCodigo.getText());
//        ag.setCodigo(ag.codigoAgendamento(txtData, this.tconsultado, consultor));
        
        
        this.setCodAgendamento(ag.codigoAgendamento(txtData, this.tconsultado, consultor));

        txtCodigo.setText(this.getCodAgendamento());
        
        if(!txtId.getText().equals("")){
            ag.setIdagendamento(Integer.valueOf(txtId.getText()));

            if(ag.alterarAgendamento()){
                preencheTexto("Agendamento salvo com sucesso!");
                if(JOptionPane.showConfirmDialog(null, "Deseja imprimir o comprovante ?", "SUCESSO", JOptionPane.YES_NO_OPTION) == 0){
                    rel = new Relatorios();
                    rel.geraComprovanteAtendimento(txtCodigo.getText());
                }
                limpaCampos();
                preencheTabAgendamento();
            }

        }else{
            if(ag.verificaExistente()){
                preencheTexto("Este agendamento é existente. Altere ou exclua para registrar.");
            }else{
                if(ag.incluirAgendamento()){
                    preencheTexto("Agendamento salvo com sucesso!");
                    if(JOptionPane.showConfirmDialog(null, "Deseja imprimir o comprovante ?", "SUCESSO", JOptionPane.YES_NO_OPTION) == 0){
                        rel = new Relatorios();
                        rel.geraComprovanteAtendimento(txtCodigo.getText());
                    }
                    preencheTexto("Código do Agendamento: " + this.getCodAgendamento());
                    limpaCampos();
                    preencheTabAgendamento();
                }
            }
        }

        
        // TODO add your handling code here:
    }//GEN-LAST:event_btSalvarActionPerformed

    public void preencheTexto(String texto){
        campoTexto.setLineWrap(true);
        campoTexto.append("\n" + texto);
    }
    

    
